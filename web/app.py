    desc = wx.get("desc_en", wx["desc"]).lower()
    humidity = int(wx["humidity"])
    wind = int(wx["wind"])

    if temp >= 33:
        bring += ["轻薄衣物", "防晒霜", "墨镜", "防晒帽"]
        skip += ["外套", "厚衣物"]
    elif temp >= 26:
        bring += ["短袖", "透气衣物"]
        skip += ["厚外套", "羽绒服"]
        if humidity > 75:
            bring.append("薄外套（防室内冷气）")
    elif temp >= 18:
        bring += ["薄外套", "长袖"]
        skip += ["羽绒服", "夏装"]
    elif temp >= 10:
        bring += ["外套", "厚长裤"]
        skip += ["夏装", "短袖"]
    elif temp >= 3:
        bring += ["厚外套", "保暖内衣", "围巾"]
        skip += ["单薄衣物", "短袖"]
    else:
        bring += ["羽绒服", "手套", "围巾", "厚袜子"]
        skip += ["单薄衣物"]

    rain_words = ["rain", "drizzle", "shower", "thunder"]
    snow_words = ["snow", "blizzard", "sleet"]
    if any(w in desc for w in rain_words):
        bring.append("雨伞 / 雨衣")
    else:
        skip.append("雨伞")
    if any(w in desc for w in snow_words):
        bring += ["防滑靴", "保暖手套"]

    if humidity > 80 and temp >= 24:
        bring.append("备用换洗衣物")
    if temp >= 28 and ("clear" in desc or "sunny" in desc):
        if "防晒霜" not in bring:
            bring.append("防晒霜")
    if wind > 40:
        bring.append("防风外套")

    return bring[:6], skip[:4]


